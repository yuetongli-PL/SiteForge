function siteUrl(report, result) {
  return report.site?.root_url ?? report.site?.input_url ?? result?.inputUrl ?? 'https://example.invalid/';
}

function siteHost(report, result) {
  try {
    return new URL(siteUrl(report, result)).hostname.replace(/^www\./u, '');
  } catch {
    return siteUrl(report, result);
  }
}

function safeFailureCode(value) {
  const text = String(value ?? '').trim().toLowerCase();
  return /^[a-z0-9][a-z0-9._-]{0,120}$/u.test(text) ? text : null;
}

function publicFailureReason(code) {
  if (code === 'robots-unavailable') return '无法取得 robots.txt，实时构建已安全停止。';
  if (code === 'robots-disallowed' || code === 'setup-robots-disallowed' || code === 'setup-known-policy-robots-disallowed') return 'robots.txt 阻止了本次候选采集范围，SiteForge 不会绕过 robots 继续抓取。';
  if (code === 'anti-crawl-verify') return '页面返回验证码、风控或安全校验中间页，SiteForge 已停止自动采集且不会绕过验证。';
  if (code === 'blocked-by-cloudflare-challenge') return '页面触发 Cloudflare challenge，SiteForge 已停止自动采集且不会绕过 challenge。';
  if (code === 'network-fetch-failed') return '网络抓取失败，未保存原始错误详情或敏感材料。';
  if (code === 'dynamic-unsupported') return '当前路径需要动态采集能力，本次没有取得足够的脱敏结构证据。';
  if (code === 'page-reconciliation-failed') return '页面核对未通过：页面分类链接、能力或意图证据与实际观察到的公开结构不匹配。';
  if (code === 'validation-failed') return '验证未通过，详细门禁结果见 verification_report.json';
  return null;
}

function failedBuildReasons(report = /** @type {any} */ ({})) {
  const reasons = /** @type {any[]} */ ([]);
  const codes = [
    report.reason_code,
    report.reasonCode,
    report.reason,
  ].map((value) => safeFailureCode(value)).filter(Boolean);
  for (const code of codes) {
    const publicReason = publicFailureReason(code);
    if (publicReason) {
      reasons.push(publicReason);
    }
  }
  const sanitizedReason = String(report.reason ?? '').trim();
  if (!reasons.length && sanitizedReason && !safeFailureCode(sanitizedReason) && sanitizedReason !== report.failure_class) {
    reasons.push(sanitizedReason);
  }
  if (!reasons.length && report.failure_class) {
    reasons.push(`失败分类：${String(report.failure_class).trim()}`);
  }
  return [...new Set(reasons)];
}


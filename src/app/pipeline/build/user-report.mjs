function normalizeText(value) {
  return String(value ?? '').trim().toLowerCase();
}

const DEBUG_ONLY_STATUS_VALUES = new Set(['debug_only', 'candidate_debug_only']);

function isDebugOnlyCapability(capability = {}) {
  if (capability.debug_only === true || capability.debugOnly === true || capability.candidate_debug_only === true) {
    return true;
  }
  if (
    capability.autoGenerated === true
    && capability.informational === true
    && normalizeText(capability.enabled_status) === 'disabled'
    && ['read_public_low', 'read_personal_medium'].includes(normalizeText(capability.risk_level ?? capability.riskPolicy?.riskLevel))
  ) {
    return true;
  }
  const values = [
    capability.enabled_status,
    capability.enabledStatus,
    capability.default_policy,
    capability.defaultPolicy,
    capability.strategy,
    capability.reason_code,
    capability.reasonCode,
    capability.evidence_status,
    capability.status,
  ].map(normalizeText);
  return values.some((value) => DEBUG_ONLY_STATUS_VALUES.has(value))
    || normalizeText(capability.status) === 'candidate';
}

function capabilitySortText(capability = {}) {
  return [
    capability.category,
    capability.name,
    capability.user_facing_name,
    capability.userFacingName,
    capability.userValue,
    capability.action,
    capability.object,
  ].filter(Boolean).join(' ').toLowerCase();
}

function capabilityUserSortRank(capability = {}) {
  const text = capabilitySortText(capability);
  const category = normalizeText(capability.category);
  const riskLevel = normalizeText(capability.risk_level);
  const enabledStatus = normalizeText(capability.enabled_status);
  const defaultPolicy = normalizeText(capability.default_policy);
  if (
    ['write_high', 'account_security_critical'].includes(riskLevel)
    || (enabledStatus === 'disabled' && /account|security|settings|delete|follow|like|repost|upload|payment|checkout|publish|send/u.test(text))
  ) {
    return 80;
  }
  if (riskLevel === 'write_low' || defaultPolicy === 'draft_only' || /draft|compose|reply draft|quote-post draft|post draft/u.test(text)) {
    return 60;
  }
  if (category === 'reading') return 10;
  if (category === 'search') return 20;
  if (category === 'profile') return 30;
  if (category === 'post_detail') return 40;
  if (category === 'navigation') return 45;
  if (/notification|bookmark|\blists?\b|direct message|\bdm\b|following|followers|personal|account profile|timeline/u.test(text)) {
    return 50;
  }
  if (/post detail|post thread|reply tree|replies|quote|media|article detail|product detail|detail/u.test(text)) {
    return 40;
  }
  if (/profile|author|creator|homepage content/u.test(text)) {
    return 30;
  }
  if (/search|query|filter/u.test(text)) {
    return 20;
  }
  if (/homepage|home page|view|browse|timeline|feed|article|product|news|content/u.test(text)) {
    return 10;
  }
  if (enabledStatus === 'disabled' || normalizeText(capability.status) === 'disabled') {
    return 70;
  }
  if (/nav|navigate|route|menu|tab|modal|external|explore|open/u.test(text)) {
    return 65;
  }
  return 55;
}

function filterAndSortUserCapabilities(capabilities = []) {
  return (Array.isArray(capabilities) ? capabilities : [])
    .filter((capability) => !isDebugOnlyCapability(capability))
    .sort((left, right) => (
      capabilityUserSortRank(left) - capabilityUserSortRank(right)
      || capabilityName(left).localeCompare(capabilityName(right), 'zh-Hans')
      || String(left.id ?? '').localeCompare(String(right.id ?? ''), 'en')
    ));
}

function hasCjk(value) {
  return /[\u3400-\u9fff]/u.test(String(value ?? ''));
}

function friendlyCapabilityLabel(value) {
  const text = String(value ?? '').trim();
  if (!text) return null;
  const mapped = FRIENDLY_CAPABILITY_NAMES.get(normalizeText(text));
  if (mapped) return mapped;
  if (hasCjk(text)) return text;
  if (/candidate only until instrumentation/i.test(text)) return '脱敏网络接口候选';
  if (/account security/i.test(text)) return '修改账号安全设置';
  if (/account settings/i.test(text)) return '修改账号设置';
  if (/follow account/i.test(text)) return '关注账号';
  if (/draft quote/i.test(text)) return '创建转发草稿';
  if (/draft reply/i.test(text)) return '创建回复草稿';
  if (/draft post/i.test(text)) return '创建发帖草稿';
  if (/direct message/i.test(text)) return '私信相关能力';
  if (/bookmark/i.test(text)) return '书签相关能力';
  if (/notification/i.test(text)) return '通知相关能力';
  if (/timeline/i.test(text)) return '时间线相关能力';
  if (/search/i.test(text)) return '搜索相关能力';
  if (/profile/i.test(text)) return '个人主页相关能力';
  return text;
}

function capabilityName(capability = {}) {
  for (const value of [
    capability.user_facing_name,
    capability.userFacingName,
    capability.userValue,
    capability.name,
    capability.id,
  ]) {
    const label = friendlyCapabilityLabel(value);
    if (label) return label;
  }
  return '未命名能力';
}

function capabilityReason(capability = {}) {
  const reasonCode = normalizeText(capability.reason_code);
  if (INTERNAL_REASON_TEXT.has(reasonCode)) return INTERNAL_REASON_TEXT.get(reasonCode);
  const reason = String(capability.reason ?? '').trim();
  const reasonText = normalizeText(reason);
  if (INTERNAL_REASON_TEXT.has(reasonText)) return INTERNAL_REASON_TEXT.get(reasonText);
  if (
    normalizeText(capability.strategy) === 'candidate_debug_only'
    || normalizeText(capability.default_policy) === 'candidate_debug_only'
    || normalizeText(capability.enabled_status) === 'candidate_debug_only'
    || normalizeText(capability.status) === 'candidate'
  ) {
    return '当前仅作为调试候选，尚未满足默认启用条件';
  }
  if (reason && hasCjk(reason) && !/^[a-z0-9_.:-]+$/iu.test(reason)) return reason;
  if (capability.risk_level === 'read_personal_medium') return '属于敏感只读范围';
  if (capability.risk_level === 'read_private_high') return '属于高敏感范围';
  if (capability.risk_level === 'write_low') return '写入草稿需要确认';
  if (capability.risk_level === 'write_high') return '高风险写入操作';
  if (capability.risk_level === 'account_security_critical') return '账号安全关键操作';
  return '需要更多证据或用户确认';
}

function capabilityStrategy(capability = {}) {
  const strategy = normalizeText(capability.strategy);
  if (INTERNAL_STRATEGY_TEXT.has(strategy)) return INTERNAL_STRATEGY_TEXT.get(strategy);
  if (normalizeText(capability.enabled_status) === 'limited_enabled') return '只保存脱敏结构摘要，不保存正文';
  if (capability.risk_level === 'read_personal_medium') return '只记录脱敏结构和数量，不保存正文';
  if (capability.risk_level === 'read_private_high') return '默认不读取正文，只允许受限摘要或保持禁用';
  if (capability.risk_level === 'write_low') return '只生成草稿，不提交';
  return '默认禁用，不自动执行';
}

function isBlockedUserCapability(capability = {}) {
  return normalizeText(capability.confirmation_group) === 'blocked'
    || capability.ordinary_confirmation_allowed === false
    || normalizeText(capability.enabled_status) === 'disabled'
    || normalizeText(capability.status) === 'disabled'
    || ['write_high', 'account_security_critical', 'read_private_high'].includes(normalizeText(capability.risk_level));
}

function safePathForCapability(capability = {}) {
  const riskLevel = normalizeText(capability.risk_level);
  const status = normalizeText(capability.enabled_status ?? capability.status);
  if (riskLevel === 'read_private_high') {
    return '仅可补脱敏结构摘要、数量和可验证路由，不补正文或会话内容';
  }
  if (riskLevel === 'write_low' || normalizeText(capability.default_policy) === 'draft_only') {
    return '仅可补草稿生成和 dry-run 验证计划，不补提交动作';
  }
  if (riskLevel === 'write_high') {
    return '仅可补只读预览、草稿替代或人工确认计划，不补自动写入';
  }
  if (riskLevel === 'account_security_critical') {
    return '仅可补账号设置的只读说明和人工操作指引，不补自动改动';
  }
  if (status === 'disabled') {
    return '可补脱敏证据和站点专用安全计划；通过复审前保持禁用';
  }
  return '可补脱敏证据和验证计划，通过后仍按安全策略确认';
}

function nonRepairableReasonForCapability(capability = {}) {
  const riskLevel = normalizeText(capability.risk_level);
  const text = capabilitySortText(capability);
  if (/direct message|private message|\bdm\b/u.test(text)) {
    return '涉及私信正文、收件人或发送边界，不能通过普通确认启用';
  }
  if (riskLevel === 'account_security_critical') {
    return '涉及账号安全或付款设置，不能自动启用';
  }
  if (riskLevel === 'write_high') {
    return '涉及发布、删除、关注、点赞、上传或发送等写入动作，不能直接启用';
  }
  if (riskLevel === 'read_private_high') {
    return '可能包含私密正文或私人会话内容，不能按默认路径读取';
  }
  if (normalizeText(capability.enabled_status) === 'disabled' || normalizeText(capability.status) === 'disabled') {
    return '当前没有已验证的安全执行路径，默认保持禁用';
  }
  return '缺少满足门禁的能力级证据或安全计划';
}

function nextStepForBlockedCapability(capability = {}) {
  const reviewCommand = String(capability.review_command ?? '').trim();
  const nextStep = String(capability.next_step ?? '').trim();
  if (reviewCommand && !path.isAbsolute(reviewCommand)) {
    return reviewCommand;
  }
  if (nextStep && hasCjk(nextStep) && !/[A-Za-z]:\\|\/Users\/|\/home\/|cookie|token|authorization/iu.test(nextStep)) {
    return nextStep;
  }
  return '保持禁用；如确需支持，先实现站点专用安全路径和验证计划，再重新运行构建';
}

function displayReportPath(value, options = {}) {
  const reportPath = String(value ?? '').trim();
  if (!reportPath) {
    return '-';
  }
  if (path.win32.isAbsolute(reportPath)) {
    const cwd = String(options.cwd ?? process.cwd());
    if (path.win32.isAbsolute(cwd)) {
      const relative = path.win32.relative(cwd, reportPath);
      if (relative && !relative.startsWith('..') && !path.win32.isAbsolute(relative)) {
        return relative.replace(/\\/gu, '/');
      }
    }
    return path.win32.basename(reportPath);
  }
  if (!path.isAbsolute(reportPath)) {
    return reportPath.replace(/\\/gu, '/');
  }
  const cwd = options.cwd ?? process.cwd();
  const relative = path.relative(cwd, reportPath);
  if (relative && !relative.startsWith('..') && !path.isAbsolute(relative)) {
    return relative.replace(/\\/gu, '/');
  }
  return path.basename(reportPath);
}

function compactCell(value, maxLength = 48) {
  const text = String(value ?? '-')
    .replace(/\r?\n/gu, ' ')
    .replace(/\s+/gu, ' ')
    .replace(/\|/gu, '/')
    .trim();
  if (!text) return '-';
  const chars = [...text];
  if (chars.length <= maxLength) return text;
  return `${chars.slice(0, Math.max(0, maxLength - 1)).join('')}…`;
}

function appendTable(lines, columns, rows, options = {}) {
  const indent = options.indent ?? '  ';
  lines.push(`${indent}| ${columns.map((column) => column.label).join(' | ')} |`);
  lines.push(`${indent}| ${columns.map(() => '---').join(' | ')} |`);
  if (!rows.length) {
    lines.push(`${indent}| - | ${columns.slice(1).map(() => '-').join(' | ')} |`);
    return;
  }
  for (const row of rows) {
    lines.push(`${indent}| ${columns.map((column) => {
      const value = typeof column.value === 'function' ? column.value(row) : row[column.key];
      return compactCell(value, column.maxLength ?? 48);
    }).join(' | ')} |`);
  }
}

function padTreeLeft(value, width = 36) {
  const text = String(value ?? '');
  const length = [...text].length;
  if (length >= width) return text;
  return `${text}${' '.repeat(width - length)}`;
}

function appendTreeLine(lines, left, right = '') {
  lines.push(right ? `${padTreeLeft(left)} │ ${right}` : left);
}

function capabilityCompactReason(capability) {
  return capabilityStrategy(capability) || capabilityReason(capability) || '-';
}

function appendTreeCapabilityRows(lines, capabilities, options = {}) {
  const limit = options.limit ?? 8;
  const checkbox = options.checkbox ?? '[ ]';
  const items = filterAndSortUserCapabilities(capabilities);
  if (!items.length) {
    appendTreeLine(lines, '    - 无', '-');
    return;
  }
  for (const capability of items.slice(0, limit)) {
    appendTreeLine(
      lines,
      `    ${checkbox} ${capabilityName(capability)}`,
      compactCell(capabilityCompactReason(capability), 52),
    );
  }
  const remaining = Math.max(0, items.length - limit);
  if (remaining > 0) {
    appendTreeLine(lines, `    … 另有 ${remaining} 项`, '详见报告');
  }
}

function safeCommandUrl(value) {
  try {
    const parsed = new URL(String(value ?? 'https://example.invalid/'));
    parsed.username = '';
    parsed.password = '';
    parsed.pathname = '/';
    parsed.search = '';
    parsed.hash = '';
    return parsed.toString();
  } catch {
    return 'https://example.invalid/';
  }
}

function appendCapabilityList(lines, title, capabilities, marker, limit) {
  lines.push('', title);
  appendCapabilityTableRows(lines, capabilities, marker, limit);
  return;
  const items = filterAndSortUserCapabilities(capabilities);
  const displayItems = [];
  const seenNames = new Set();
  for (const capability of items) {
    const name = capabilityName(capability);
    const key = `${marker}:${name}`;
    if (seenNames.has(key)) continue;
    seenNames.add(key);
    displayItems.push({ capability, name });
  }
  if (!items.length) {
    lines.push('  - 无');
    return;
  }
  if (marker === '×') {
    lines.push('  说明：自动补的是安全路径和验证计划，不是绕过禁用直接启用。');
  }
  for (const { capability, name } of displayItems.slice(0, limit)) {
    lines.push(`  ${marker} ${name}`);
    if (marker !== '✓') {
      lines.push(`    原因：${capabilityReason(capability)}`);
      lines.push(`    策略：${capabilityStrategy(capability)}`);
      if (marker === '×' || isBlockedUserCapability(capability)) {
        lines.push(`    可补安全路径：${safePathForCapability(capability)}`);
        lines.push(`    不可补原因：${nonRepairableReasonForCapability(capability)}`);
        lines.push(`    下一步：${nextStepForBlockedCapability(capability)}`);
      }
    }
  }
  const remainingCount = Math.max(0, displayItems.length - limit);
  if (remainingCount > 0) {
    lines.push(`  ... 另有 ${remainingCount} 项已写入报告`);
  }
}

function capabilityStatusLabel(statusKey) {
  switch (statusKey) {
    case 'enabled':
      return '启用';
    case 'limited':
      return '有限';
    case 'confirmation':
      return '确认';
    case 'disabled':
      return '禁用';
    default:
      return String(statusKey ?? '-');
  }
}

function appendCapabilityTableRows(lines, capabilities, statusKey, limit) {
  const items = filterAndSortUserCapabilities(capabilities);
  const displayItems = [];
  const seenNames = new Set();
  for (const capability of items) {
    const name = capabilityName(capability);
    const key = `${statusKey}:${name}`;
    if (seenNames.has(key)) continue;
    seenNames.add(key);
    displayItems.push({ capability, name });
  }
  if (!displayItems.length) {
    appendTable(lines, [
      { key: 'status', label: '状态', maxLength: 8 },
      { key: 'capability', label: '能力', maxLength: 28 },
      { key: 'policy', label: '处理方式', maxLength: 36 },
    ], [{ status: '-', capability: '无', policy: '-' }]);
    return;
  }
  const statusLabel = capabilityStatusLabel(statusKey);
  const rows = displayItems.slice(0, limit).map(({ capability, name }, index) => {
    const blocked = statusKey === 'disabled' || isBlockedUserCapability(capability);
    return {
      index: index + 1,
      status: statusLabel,
      capability: name,
      reason: statusKey === 'enabled' ? '-' : capabilityReason(capability),
      policy: statusKey === 'enabled' ? '默认启用' : capabilityStrategy(capability),
      safePath: blocked ? safePathForCapability(capability) : '-',
      nextStep: blocked ? nextStepForBlockedCapability(capability) : '-',
    };
  });
  const columns = statusKey === 'enabled'
    ? [
      { key: 'index', label: '#', maxLength: 4 },
      { key: 'status', label: '状态', maxLength: 8 },
      { key: 'capability', label: '能力', maxLength: 32 },
      { key: 'policy', label: '处理方式', maxLength: 28 },
    ]
    : statusKey === 'disabled'
      ? [
        { key: 'index', label: '#', maxLength: 4 },
        { key: 'capability', label: '能力', maxLength: 28 },
        { key: 'reason', label: '不可补原因', maxLength: 34 },
        { key: 'safePath', label: '安全补路径', maxLength: 38 },
        { key: 'nextStep', label: '下一步', maxLength: 34 },
      ]
      : [
        { key: 'index', label: '#', maxLength: 4 },
        { key: 'status', label: '状态', maxLength: 8 },
        { key: 'capability', label: '能力', maxLength: 28 },
        { key: 'reason', label: '原因', maxLength: 34 },
        { key: 'policy', label: '处理方式', maxLength: 38 },
      ];
  appendTable(lines, columns, rows);
  const remainingCount = Math.max(0, displayItems.length - limit);
  if (remainingCount > 0) {
    const fillerCells = Array.from({ length: Math.max(0, columns.length - 3) }, () => '').join(' | ');
    const filler = fillerCells ? ` | ${fillerCells}` : '';
    lines.push(`  | … | 另有 ${remainingCount} 项 | 详见报告${filler} |`);
  }
}

